def portfolio_industry_breakdown(
    holdings: dict,
    price_data: dict[str, float],
    code_industry_map: dict[str, str],
) -> dict[str, float]:
    """按行业汇总持仓权重，返回按权重降序排列的 {行业: 权重}"""
    total_mv = sum(
        info['shares'] * price_data.get(code, 0)
        for code, info in holdings.items()
        if code in price_data and price_data[code] > 0
    )
    if total_mv <= 0:
        return {}
    industry_weights: dict[str, float] = {}
    for code, info in holdings.items():
        if code in price_data and price_data[code] > 0:
            mv = info['shares'] * price_data[code]
            industry = code_industry_map.get(code, "其他")
            industry_weights[industry] = industry_weights.get(industry, 0) + mv / total_mv
    return dict(sorted(industry_weights.items(), key=lambda x: x[1], reverse=True))

def cap_industry_weights(
    target_weights: dict[str, float],
    code_industry_map: dict[str, str],
    max_industry_weight: float = 0.25,
) -> tuple[dict[str, float], dict]:
    """
    行业仓位上限约束。超限行业等比压缩，释放的权重按比例分配给其他行业。
    """
    if not target_weights or max_industry_weight <= 0:
        return target_weights, {"applied": False, "reason": "disabled"}

    # 按行业汇总
    industry_totals: dict[str, list] = {}
    for code, w in target_weights.items():
        ind = code_industry_map.get(code, "其他")
        if ind not in industry_totals:
            industry_totals[ind] = []
        industry_totals[ind].append(code)

    industry_sums = {ind: sum(target_weights[c] for c in codes) for ind, codes in industry_totals.items()}
    violated = {ind: s for ind, s in industry_sums.items() if s > max_industry_weight}

    if not violated:
        return target_weights, {"applied": False, "industry_sums": industry_sums}

    # 超限行业压缩
    adjusted = dict(target_weights)
    remaining_budget = 1.0
    normal_industries = []

    for ind, codes in industry_totals.items():
        if ind in violated:
            scale = max_industry_weight / industry_sums[ind]
            for c in codes:
                adjusted[c] = target_weights[c] * scale
            remaining_budget -= max_industry_weight
        else:
            normal_industries.append(ind)

    # 剩余预算分配给非超限行业
    normal_total = sum(industry_sums[ind] for ind in normal_industries)
    if normal_total > 0:
        for ind in normal_industries:
            scale = remaining_budget / normal_total
            for c in industry_totals[ind]:
                adjusted[c] = target_weights[c] * scale

    return adjusted, {
        "applied": True,
        "violated": violated,
        "before": industry_sums,
        "after": {ind: sum(adjusted.get(c, 0) for c in codes) for ind, codes in industry_totals.items()},
    }
